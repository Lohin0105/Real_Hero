/**
 * Get translated string with variable replacement.
 * @param {string} langCode - The 2-letter language code (e.g., 'en', 'hi').
 * @param {string} key - The translation key.
 * @param {object} params - Key-value pairs for variable replacement.
 * @returns {string} - The translated string.
 */
export const getTranslation = (langCode, key, params = {}) => {
    const lang = translations[langCode] || translations['en'];
    let text = lang[key] || translations['en'][key] || key;

    // Replace placeholders like {{name}}
    Object.keys(params).forEach(param => {
        const regex = new RegExp(`{{${param}}}`, 'g');
        text = text.replace(regex, params[param]);
    });

    return text;
};
